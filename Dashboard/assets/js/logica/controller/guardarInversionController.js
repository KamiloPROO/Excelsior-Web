function GuardarPlanActivado() {

    var idpersona = localStorage.getItem("idPersona");

    var idPlan = localStorage.getItem("idPlan0");


    const GuardarPlan = {
        
        planid: idPlan,
        IdPersona: idpersona,
        totalvalue: $("#montoOperarPlan0").val(),
        address: "",
        pk: ""
    }

    guardar(GuardarPlan);
   
}

